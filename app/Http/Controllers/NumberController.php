    public function index()
    {
        $numbers = Number::latest()->paginate(2);
        if ($key = request()->key) {
            $numbers = Number::orderBy('id', 'desc')->where('mathietbi','LIKE','%'.$key."%")->paginate(5);
        }
        return view('number.index',compact('numbers'))
                 ->with('i', (request()->input('page', 1) - 1) * 5);
    }

